class Movie {
  final int id;
  final String title;
  final String overview;
  final String posterUrl;
  final String releaseYear;
  final double rating;
  final String genre;

  Movie({
    required this.id,
    required this.title,
    required this.overview,
    required this.posterUrl,
    required this.releaseYear,
    required this.rating,
    required this.genre,
  });
}
